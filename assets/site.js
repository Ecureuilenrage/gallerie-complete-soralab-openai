function getInitialLanguage() {
  const stored = localStorage.getItem('ocl-language');
  if (stored === 'fr' || stored === 'en') return stored;
  const pageLang = document.documentElement.lang;
  return pageLang === 'en' ? 'en' : 'fr';
}

